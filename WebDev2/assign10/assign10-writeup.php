<h3>What is PHPMyAdmin</h3>
<p>PHPMyAdmin is a graphical front-end for managing MySQL databases. It automates common database management processes such as uploading tables, exporting information, queries, managing users and privileges, and on and on.</p>
<p>Link 1:  <a href="https://classweb.ccv.edu/etc/apps/phpmyadmin/doc/html/intro.html" target="_blank">link</a></p>

<h3>Back up tables</h3>
<p>PHPMyAdmin does not do a strict backup in the classical sense. Under the export tab there is the option for PHPMyAdmin to write out to a couple different file formats the data, structure, or both. It can even write out a .sql file that can recreate all of the tables and reinput all of the data. I would hate to write that program....</p>
<p>Link 2:  <a href="https://classweb.ccv.edu/etc/apps/phpmyadmin/doc/html/faq.html#using-phpmyadmin" target="_blank">link</a></p>

<h3>Manually add and update records</h3>
<p>In the navigation tab select the "SQL" button. This provides a text box that you can input SQL statements into.</p>
